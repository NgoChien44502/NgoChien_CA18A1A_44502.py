nam = 365
gio = 24*nam
phut = gio *60
giay = phut *60
vantocanhsang = 3*10**8
anhsangtrongnam = vantocanhsang*giay
print("Du lịch trong năm:"+str(anhsangtrongnam)+ "m/nam")





